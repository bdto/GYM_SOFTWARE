    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nombre = inputNombre.getText();
        String apellido = inputApellido.getText();
        String address = inputAddress.getText();
        String phone = inputPhone.getText();
        Object gender = comboboxgender.getSelectedItem();
        String referidoPor = (String) comboboxReferidoPor.getSelectedItem();
        String numeroStr = referidoPor.split(" - ")[0];
        int idReferido = Integer.parseInt(numeroStr);
        
        String sqlCreateClient = "insert into clientes(nombre, apellido, telefono, genero, direccion, id_admin_referido) values ('"+nombre+"', '"+apellido+"', '"+phone+"','"+gender+"','"+address+"','"+idReferido+"');";
        try {
            System.out.println(sqlCreateClient);
            Conexion conexion = new Conexion();
            conexion.ejecutarUpdate(sqlCreateClient);
            JOptionPane.showMessageDialog(this, "Cliente creado con exito.");
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, "Error al obtener los datos de la base de datos.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

