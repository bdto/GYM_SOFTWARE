    private void cargarReferidos() {
        try {
            Conexion conexion = new Conexion();
            String query = "SELECT id, nombre FROM admins WHERE estado = 'Active'";
            List<Map<String, Object>> resultados = conexion.ejecutarConsultaVariosResultados(query);
            
            for (Map<String, Object> fila : resultados) {
                int id = (int) fila.get("id");
                String nombre = (String) fila.get("nombre");
                comboboxReferidoPor.addItem(id + " - " + nombre);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
