    private void cargarReferidos() {
        try {
            Conexion conexion = new Conexion();
            String query = "select id, concat(c.id, '-', c.nombre, ' ', c.apellido) as nombre\n" +
                "from clientes c \n" +
                "where c.estado = 'ACTIVE';";
            List<Map<String, Object>> resultados = conexion.ejecutarConsultaVariosResultados(query);
            
            for (Map<String, Object> fila : resultados) {
                String nombre = (String) fila.get("nombre");
                comboboxCliente.addItem(nombre);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
