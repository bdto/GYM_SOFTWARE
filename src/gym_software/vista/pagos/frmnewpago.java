    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String tipoSuscripcion = (String) comboboxTipoSuscripcion.getSelectedItem();
        String precio = inputPrecio.getText();
        String duracionDias = inputDuracionDias.getText();
        String cliente = (String) comboboxCliente.getSelectedItem();
        String numeroStr = cliente.split("-")[0];
        int idCliente = Integer.parseInt(numeroStr);
        
        String sqlCreateClient = "insert into pagos(tipo_suscripcion, precio, duracion, id_cliente) values ('"+tipoSuscripcion+"', '"+precio+"', '"+duracionDias+"','"+idCliente+"');";
        try {
            System.out.println(sqlCreateClient);
            Conexion conexion = new Conexion();
            conexion.ejecutarUpdate(sqlCreateClient);
            JOptionPane.showMessageDialog(this, "Pago creado con exito.");
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, "Error al obtener los datos de la base de datos.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

